let peca = "Peao";
let pecaComLowerCase = peca.toLowerCase();

switch(pecaComLowerCase){
    case "rainha": 
        console.log("É a rainha");
        break;
    case "rei":
        console.log("É o rei");
        break;
    case "peao":
        console.log("É o peão");
        break;
    case "torre":
        console.log("É a torre");
        break;
    case "bispo":
        console.log("É o bispo");
        break;
    case "cavalo":
        console.log("É o cavalo");
        break;
      
}